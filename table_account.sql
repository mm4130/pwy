  id int NOT NULL AUTO_INCREMENT PRIMARY KEY ,
  pseudo varchar(50),
  mdp varchar(50),
  lvl int DEFAULT 0,
  xp int DEFAULT 0,
  mission varchar(200),
  missionRecompense int DEFAULT 0,
  sponsor varchar(50)
)  DEFAULT CHARACTER SET utf8 COLLATE utf8_unicode_ci;

